-- creazione database
CREATE DATABASE ecommerce;

-- creazione tabelle delle entità
CREATE TABLE ecommerce.tipo_utente (
    id INT PRIMARY KEY,
    descrizione VARCHAR(255)
);

CREATE TABLE ecommerce.utente (
    id INT PRIMARY KEY,
    nome VARCHAR(100),
    cognome VARCHAR(100),
    email VARCHAR(100),
    username VARCHAR(50),
    password VARCHAR(255),
    tipologia_id INT,
    FOREIGN KEY (tipologia_id) REFERENCES ecommerce.tipo_utente(id)
);

CREATE TABLE ecommerce.abbonamento (
    id INT PRIMARY KEY,
    nome VARCHAR(100),
    prezzo DECIMAL(10,2),
    n_cioccolata INT
);

CREATE TABLE ecommerce.magazzino (
    id INT PRIMARY KEY,
    quantita INT,
    disponibilita VARCHAR(5) CHECK (disponibilita IN ('si', 'no', 'forse'))
);

CREATE TABLE ecommerce.tipo_cioccolata (
    id INT PRIMARY KEY,
    nome VARCHAR(100)
);

CREATE TABLE ecommerce.qualita_cioccolata (
    id INT PRIMARY KEY,
    punteggio INT,
    descrizione VARCHAR(255)
);

CREATE TABLE ecommerce.cioccolata (
    id INT PRIMARY KEY,
    nome VARCHAR(100),
    tipo_id INT,
    anno_produzione INT,
    prezzo_vendita DECIMAL(10,2),
    FOREIGN KEY (tipo_id) REFERENCES ecommerce.tipo_cioccolata(id)
);

CREATE TABLE ecommerce.recensione (
    id INT PRIMARY KEY,
    descrizione VARCHAR(255),
    utente_id INT,
    cioccolata_id INT,
    FOREIGN KEY (utente_id) REFERENCES ecommerce.utente(id),
    FOREIGN KEY (cioccolata_id) REFERENCES ecommerce.utente(id)
);

-- creazione tabelle delle relazioni 
CREATE TABLE ecommerce.iscrizione (
    utente_id INT,
    abbonamento_id INT,
    data_inizio DATE,
    data_fine DATE,
    PRIMARY KEY (utente_id, abbonamento_id),
    FOREIGN KEY (utente_id) REFERENCES ecommerce.utente(id),
    FOREIGN KEY (abbonamento_id) REFERENCES ecommerce.abbonamento(id)
);

CREATE TABLE ecommerce.commento (
    utente_id INT,
    recensione_id INT,
    testo VARCHAR(255),
    data_commento DATE,
    PRIMARY KEY (utente_id, recensione_id),
    FOREIGN KEY (utente_id) REFERENCES ecommerce.utente(id),
    FOREIGN KEY (recensione_id) REFERENCES ecommerce.recensione(id)
);

CREATE TABLE ecommerce.acquisto (
    utente_id INT,
    cioccolata_id INT,
    data_acquisto DATE,
    quantita INT,
    PRIMARY KEY (utente_id, cioccolata_id, data_acquisto),
    FOREIGN KEY (utente_id) REFERENCES ecommerce.utente(id),
    FOREIGN KEY (cioccolata_id) REFERENCES ecommerce.cioccolata(id)
);

CREATE TABLE ecommerce.stoccaggio (
    cioccolata_id INT,
    magazzino_id INT,
    quantita INT,
    PRIMARY KEY (cioccolata_id, magazzino_id),
    FOREIGN KEY (cioccolata_id) REFERENCES ecommerce.cioccolata(id),
    FOREIGN KEY (magazzino_id) REFERENCES ecommerce.magazzino(id)
);

CREATE TABLE ecommerce.giudizio (
    recensione_id INT,
    cioccolata_id INT,
    voto INT,
    PRIMARY KEY (recensione_id, cioccolata_id),
    FOREIGN KEY (recensione_id) REFERENCES ecommerce.recensione(id),
    FOREIGN KEY (cioccolata_id) REFERENCES ecommerce.cioccolata(id)
);

CREATE TABLE ecommerce.valore (
    tipo_cioccolata_id INT,
    qualita_cioccolata_id INT,
    valore INT,
    PRIMARY KEY (tipo_cioccolata_id, qualita_cioccolata_id),
    FOREIGN KEY (tipo_cioccolata_id) REFERENCES ecommerce.tipo_cioccolata(id),
    FOREIGN KEY (qualita_cioccolata_id) REFERENCES ecommerce.qualita_cioccolata(id)
);

CREATE TABLE ecommerce.catalogazione (
    cioccolata_id INT,
    tipo_cioccolata_id INT,
    data_catalogazione DATE,
    PRIMARY KEY (cioccolata_id, tipo_cioccolata_id),
    FOREIGN KEY (cioccolata_id) REFERENCES ecommerce.cioccolata(id),
    FOREIGN KEY (tipo_cioccolata_id) REFERENCES ecommerce.tipo_cioccolata(id)
);


